def countTabs (s) :
	n = 0
	for c in s:
		if c != "\t": break
		n += 1
	return n

def a(arr, prefix="", suffix=""):
	if len(arr) == 3: # COMMAND | text | link
		text = arr[1]
		link = arr[2]
		return f"<a href='{prefix + link + suffix}'>{text}</a><br><br>"
	elif len(arr) == 4: # COMMAND | prompt | text | link
		prompt = arr[1]
		text = arr[2]
		link = arr[3]
		return f"{prompt}<a href='{prefix + link + suffix}'>{text}</a><br><br>"
	else:
		print('Error message')

